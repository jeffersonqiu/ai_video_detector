_gemini_client: genai.Client | None = None


def _get_gemini() -> genai.Client:
    global _gemini_client
    if _gemini_client is None:
        _gemini_client = genai.Client()
    return _gemini_client


# ---------------------------------------------------------------------------
# detect() — the interface the harness calls
# ---------------------------------------------------------------------------

async def detect(
    frames: list[str],
    audio_path: str | None,
    caption: str | None,
    video_path: str,
) -> dict:
    """
    3-tier routing based on caption:
      STRONG caption  → free AI GENERATED verdict
      Timelapse/transform caption → Flash + audio + 20 frames (hard case)
      Default         → Flash-Lite + audio + 20 frames (cheap)

    Returns dict with keys: verdict, confidence, reason, model_used,
    input_tokens, output_tokens, cost_usd.
    """
    # Tier 0: gradient prefilter (zero cost, CPU-only)
    if _PREFILTER_AVAILABLE:
        prefilter_result = _run_prefilter(frames[:8])
        if prefilter_result is not None:
            return {
                "verdict": prefilter_result.verdict,
                "confidence": prefilter_result.confidence,
                "reason": prefilter_result.reason,
                "model_used": prefilter_result.model_used,
                "input_tokens": 0,
                "output_tokens": 0,
                "cost_usd": 0.0,
            }

    route = _routing(caption)

    # Tier 1 (was Tier 0): free caption-only
    if route == "FREE":
        return {
            "verdict": "AI GENERATED",
            "confidence": "HIGH",
            "reason": f"Caption explicitly signals AI generation: '{(caption or '')[:80]}'",
            "model_used": "caption-only",
            "input_tokens": 0,
            "output_tokens": 0,
            "cost_usd": 0.0,
        }

    model = MODEL_FLASH if route == "FLASH" else MODEL_LITE

    # Flash tier uses 18 frames (proven sufficient), Lite uses 20 frames
    num_frames = 18 if route == "FLASH" else NUM_FRAMES

    # Select frames evenly spread across all available
    if len(frames) > num_frames:
        step = len(frames) / num_frames
        frames = [frames[int(i * step)] for i in range(num_frames)]

    has_audio = audio_path is not None and os.path.exists(audio_path or "")
    # Flash tier: audio-only (no frames) — DWmajXxjF7S is audio-driven; DVmPqQhETyD may be visual
    # Lite tier: full prompt with frames + audio
    flash_audio_only = (route == "FLASH")
    prompt = _build_prompt(caption, has_audio)

    # Build content parts
    parts: list[types.Part] = [types.Part.from_text(text=prompt)]

    if has_audio:
        with open(audio_path, "rb") as f:
            parts.append(types.Part.from_bytes(data=f.read(), mime_type="audio/mpeg"))

    # Flash tier: audio-only (skip frames) — audio is the decisive signal for subtle AI timelapses
    if not flash_audio_only:
        for frame_path in frames:
            with open(frame_path, "rb") as f:
                parts.append(types.Part.from_bytes(data=f.read(), mime_type="image/jpeg"))

    config = types.GenerateContentConfig(
        max_output_tokens=512,
        temperature=0.1,
        thinking_config=types.ThinkingConfig(thinking_budget=0),
        safety_settings=_SAFETY_SETTINGS,
    )

    response = await _get_gemini().aio.models.generate_content(
        model=model,
        contents=parts,
        config=config,
    )

    text = response.text
    usage = response.usage_metadata
    input_tokens = (usage.prompt_token_count or 0) if usage else 0
    output_tokens = (usage.candidates_token_count or 0) if usage else 0

    if text is None:
        # Blocked by safety filter
        result = {"verdict": "UNCERTAIN", "confidence": "LOW", "reason": "Gemini blocked this content."}
    else:
        result = _parse_verdict(text)
        if result is None:
            # Two-pass retry: ask model to extract verdict from its own analysis
            retry_parts = [types.Part.from_text(text=(
                "From the analysis below, extract and output ONLY these three lines:\n"
                "VERDICT: [AI GENERATED / LIKELY REAL / UNCERTAIN]\n"
                "CONFIDENCE: [HIGH / MEDIUM / LOW]\n"
                "REASON: [one sentence]\n\n"
                f"Analysis:\n{text}"
            ))]
            retry_resp = await _get_gemini().aio.models.generate_content(
                model=model, contents=retry_parts, config=config,
            )
            retry_usage = retry_resp.usage_metadata
            input_tokens += (retry_usage.prompt_token_count or 0) if retry_usage else 0
            output_tokens += (retry_usage.candidates_token_count or 0) if retry_usage else 0
            result = _parse_verdict(retry_resp.text or "") or {
                "verdict": "UNCERTAIN",
                "confidence": "LOW",
                "reason": "Could not parse model response.",
            }

    input_cost, output_cost = _MODEL_COSTS.get(model, (0.30, 1.00))
    cost_usd = (input_tokens * input_cost + output_tokens * output_cost) / 1_000_000

    return {
        "verdict": result["verdict"],
        "confidence": result["confidence"],
        "reason": result["reason"],
        "model_used": model,
        "input_tokens": input_tokens,
        "output_tokens": output_tokens,
        "cost_usd": cost_usd,
    }
