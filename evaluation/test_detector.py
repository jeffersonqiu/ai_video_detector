def _routing(caption: str | None) -> str:
    """Returns 'FREE' | 'LITE' | 'FLASH' routing decision based on caption."""
    if _caption_signal(caption) == "STRONG":
        return "FREE"
    lower = (caption or "").lower()
    if any(kw in lower for kw in _TIMELAPSE_KEYWORDS):
        return "FLASH"
    return "LITE"


