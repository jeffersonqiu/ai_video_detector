_CAPTION_ANALYSIS_WITH_SIGNAL = """\
Caption: "{caption}"
Caption AI signal detected: {signal_label}
{signal_instruction}\
"""

_AUDIO_SIGNAL_BLOCK = """\
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
SIGNAL 3 — AUDIO ANALYSIS
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
Listen to the provided audio for:
• Robotic, overly smooth, or unnaturally paced speech (TTS voice)
• Voice that doesn't match lip movements in the frames
• Unnatural pauses, breathing patterns, or speech transitions
• Synthetic-sounding background music
• Audio quality inconsistent with video quality
• Speaker mentioning AI tools or AI generation

State your audio assessment before the final verdict.

"""


def _build_prompt(caption: str | None, has_audio: bool = False) -> str:
    signal = _caption_signal(caption)

    if caption:
        if signal == "STRONG":
            signal_label = "⚠️ STRONG — caption explicitly mentions AI generation"
            signal_instruction = (
                "This is a near-definitive signal. Weight this very heavily toward AI GENERATED "
                "unless the visual evidence is overwhelmingly contradictory."
            )
        elif signal == "WEAK":
            signal_label = "⚡ WEAK — caption contains AI-related terms"
            signal_instruction = "Consider this as supporting evidence alongside visual analysis."
        else:
            signal_label = "— NEUTRAL — no AI keywords detected"
            signal_instruction = "Caption gives no direct signal either way."

        caption_block = _CAPTION_ANALYSIS_WITH_SIGNAL.format(
            caption=caption,
            signal_label=signal_label,
            signal_instruction=signal_instruction,
        ) + "\n"
    else:
        caption_block = "No caption provided — skip SIGNAL 1, rely on visual and audio only.\n\n"

    audio_hint = f"Audio provided: {'YES — analyse it in SIGNAL 3 below' if has_audio else 'NO — skip SIGNAL 3'}\n\n"
    audio_signal_block = _AUDIO_SIGNAL_BLOCK if has_audio else ""

    return _PROMPT_TEMPLATE.format(
        caption_block=caption_block,
        audio_hint=audio_hint,
        caption_analysis_instruction=caption_block.strip() if caption else "No caption — skip.",
        audio_signal_block=audio_signal_block,
    )


# ---------------------------------------------------------------------------
# Response parsing
# ---------------------------------------------------------------------------

def _parse_verdict(text: str) -> DetectionResult:
    verdict = confidence = reason = None
    for line in text.strip().splitlines():
        if line.startswith("VERDICT:"):
            verdict = line.replace("VERDICT:", "").strip()
        elif line.startswith("CONFIDENCE:"):
            confidence = line.replace("CONFIDENCE:", "").strip()
        elif line.startswith("REASON:"):
            reason = line.replace("REASON:", "").strip()

    if not all([verdict, confidence, reason]):
        logger.warning(f"Could not fully parse response: {text!r}")
        return DetectionResult(
            verdict="UNCERTAIN",
            confidence="LOW",
            reason="Could not parse model response clearly.",
            raw_response=text,
        )

    return DetectionResult(
        verdict=verdict,
        confidence=confidence,
        reason=reason,
        raw_response=text,
    )


