# Lazy singletons
_gemini_client: genai.Client | None = None
_claude_client: anthropic.AsyncAnthropic | None = None


def _get_gemini() -> genai.Client:
    global _gemini_client
    if _gemini_client is None:
        _gemini_client = genai.Client()
    return _gemini_client


def _get_claude() -> anthropic.AsyncAnthropic:
    global _claude_client
    if _claude_client is None:
        _claude_client = anthropic.AsyncAnthropic()
    return _claude_client


# ---------------------------------------------------------------------------
# Caption pre-screening — keyword-based signal before any vision call
# ---------------------------------------------------------------------------

# Phrases that near-definitively confirm AI-generated content
_CAPTION_STRONG_AI = [
    "ai generated", "ai-generated", "made with ai", "made by ai", "created by ai",
    "ai video", "ai or real", "ai vs real", "is this ai", "real or ai",
    "ai footage", "ai content", "ai challenge", "ai showcase",
    "sora", "runway ml", "pika labs", "kling ai", "luma ai", "vidu",
    "stable diffusion", "midjourney video", "deepfake", "synthetic video",
    "ai created", "generated by ai", "ai tools", "using ai",
]

# Words that weakly suggest AI involvement
_CAPTION_WEAK_AI = [
    " ai ", "#ai", "artificial", "generated", "synthetic", "avatar",
    "virtual human", "digital human", "cgi", "vfx",
]


def _caption_signal(caption: str | None) -> str:
    """
    Returns 'STRONG' | 'WEAK' | 'NONE' based on caption keywords.
    STRONG = caption near-definitively confirms AI content.
    """
    if not caption:
        return "NONE"
    lower = caption.lower()
    if any(kw in lower for kw in _CAPTION_STRONG_AI):
        return "STRONG"
    if any(kw in lower for kw in _CAPTION_WEAK_AI):
        return "WEAK"
    return "NONE"


